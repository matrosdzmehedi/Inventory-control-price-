public ArrayList<produc> getUsersList(){
ArrayList<produc> usersList = new ArrayList<produc>();
Connection connection = getConnection();
String query = "SELECT product_id,product_name,product_max_price,product_quantity,price FROM Â `Product` ";
Statement st;
ResultSet rs;
try {
st = connection.createStatement();
rs = st.executeQuery(query);
produc user;
while(rs.next())
{
user = new produc(rs.getInt("product_id"),rs.getString("product_name"),rs.getString("product_max_price"),rs.getInt("product_quantity"),rs.getInt("price"));
usersList.add(user);
}
}
catch (Exception e) {
e.printStackTrace();
}
return usersList;
}
public void Show_Users_In_JTable()
{
ArrayList<produc> list = getUsersList();
DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
Object[] row = new Object[5];
for(int i = 0; i < list.size(); i++)
{
row[0] = list.get(i).getid();
row[1] = list.get(i).getFirstName();
row[3] = list.get(i).getLastNAme();
row[2] = list.get(i).getaddrs();
row[4] = list.get(i).getmx();
model.addRow(row);
}
}

