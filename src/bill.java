    public int getsum(){
        
        int row=jTable1.getRowCount();
        int sum=0;
        for(int i=0; i<row;i++){
            sum=sum+Integer.parseInt(jTable1.getValueAt(i, 1).toString());
        }
        return sum;
        
    }

